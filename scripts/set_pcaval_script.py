import board
import busio
import adafruit_pca9685
import sys
i2c = busio.I2C(board.SCL, board.SDA)
pca = adafruit_pca9685.PCA9685(i2c)
pca.frequency = 60

list = sys.argv[1].split(',')
for n in range(0, 16):
    try:
        val = float(list[n])
        pca.channels[n].duty_cycle = int((745*val)+1005)
    except ValueError:
        continue
